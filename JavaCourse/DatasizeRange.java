import java.lang.*;

public class DatasizeRange {
    
    public static void main(String args[]){

           System.out.println("The Minimum value of Integer is : "+Integer.MIN_VALUE);
           System.out.println("The Maximum value of the Integer is : "+Integer.MAX_VALUE);
           System.out.println("The Size of the Integer is : "+Integer.BYTES);

           System.out.println("The Minimum value of Bytes is : "+Byte.MIN_VALUE);
           System.out.println("The Maximum value of the Bytes is : "+Byte.MAX_VALUE);
           System.out.println("The Size of the Bytes is : "+Byte.BYTES);

           System.out.println("The Minimum value of Float is : "+Float.MIN_VALUE);
           System.out.println("The Maximum value of the Float is : "+Float.MAX_VALUE);
           System.out.println("The Size of the Float is : "+Float.BYTES);

           System.out.println("The Minimum value of Short is : "+Short.MIN_VALUE);
           System.out.println("The Maximum value of the short is : "+Short.MAX_VALUE);
           System.out.println("The Size of the short is : "+Short.BYTES);
           
    }

}
